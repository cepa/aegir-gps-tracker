/**
  * @brief  Function implementing the mainTask thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_Main_TaskHandler */
void Main_TaskHandler(void const * argument)
{
  /* USER CODE BEGIN 5 */
  /* Infinite loop */

  Report_t report, reportDump;
  char buf[24];
  int reportStatus;

  for(;;)
  {
	// Blink LED
	HAL_GPIO_WritePin(LED_GPIO_Port, LED_Pin, GPIO_PIN_RESET);
    osDelay(200);
	HAL_GPIO_WritePin(LED_GPIO_Port, LED_Pin, GPIO_PIN_SET);
    osDelay(800);

    lteCounter++;
    iridiumCounter++;

    // Prepare report
    reportStatus = PrepareReport(&report);

    // Send report to SD card
    if (reportStatus == REPORT_OK) {
    	xQueueSend(sdQueue, &report, 10);

    	if (lteCounter > 10) {
    		lteCounter = 0;

        	// If full take the last element
        	if (uxQueueSpacesAvailable(lteQueue) == 0) {
        		xQueueReceive(lteQueue, &reportDump, 10);
        	}

        	xQueueSend(lteQueue, &report, 0);
    	}

    	if (iridiumCounter > 3600) {
    		iridiumCounter = 0;

        	// If full take the last element
        	if (uxQueueSpacesAvailable(iridiumQueue) == 0) {
        		xQueueReceive(iridiumQueue, &reportDump, 10);
    			_DEBUG("Iridium messages waiting: %d", uxQueueMessagesWaiting(iridiumQueue));
        	}

        	// Add current report
    		xQueueSend(iridiumQueue, &report, 0);
    	}
    }

    // Status update
    if (!Display_StatusIsLocked()) {
		if (lastReportSendTime > 0) {
			PrintfLock();
			snprintf(buf, 24, "Report sent %ds ago", (HAL_GetTick() - lastReportSendTime) / 1000);
			PrintfUnlock();

			Display_SetStatus(buf);
		} else {
			Display_SetStatus("Report not sent yet");
		}
    }

    // Reset Watchdog
    HAL_IWDG_Refresh(&hiwdg);
  }
  /* USER CODE END 5 */
}

