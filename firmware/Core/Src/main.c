/*
 * Override _write to forward stdout to SoftUart0
 */
int _write(int file, char *ptr, int len)
{
	int left = len;
	while (left > 0) {
		if (left > SoftUartTxBufferSize) {
			SoftUartPuts(0, (uint8_t *) ptr, SoftUartTxBufferSize);
			SoftUartWaitUntilTxComplate(0);
			left -= SoftUartTxBufferSize;
		} else {
			SoftUartPuts(0, (uint8_t *) ptr, left);
			SoftUartWaitUntilTxComplate(0);
			left -= left;
		}
	}
	return len;
}

